package aritmatika

func Aritmatika (angka1, angka2 int) int {
	var hasil int = angka1 + angka2
	// var hasil int = angka1 - angka2
	// var hasil int angka1 * angka2
	// var hasil int angka1 / angka2
	// var hasil int angka1 % angka2

	// Augmented assignment 
	hasil += angka1 + angka2
	return hasil
}

